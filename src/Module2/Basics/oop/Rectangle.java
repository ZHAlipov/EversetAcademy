package Module2.Basics.oop;

public class Rectangle {
    public int width;
    public int height;

    public void getArea() {
        System.out.printf("Площадь данного прямоугольника: %d см", width*height);
    }
    public void getPerimeter() {
        System.out.printf("Площадь данного прямоугольника: %d см", (width+height)*2);
    }

}
